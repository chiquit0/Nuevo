package ApuntesTema1VariablesyBucles;

public class TablaMultip {

	public static void main(String[] args) {
		
		int num = 2;
		
		//1º Bucle i
		for(int i=2 ; i<=2 ; i++) {
			System.out.println(num +" X " + i + " = " + (num*i));
		//2º Bucle j
		for(int j=1 ; j<10 ; j++) {
			System.out.println(i + " X " + j + " = " + (i*j));
				
			}

	}

} }
